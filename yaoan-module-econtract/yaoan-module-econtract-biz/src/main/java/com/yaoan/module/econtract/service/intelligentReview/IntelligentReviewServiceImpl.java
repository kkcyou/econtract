    public static long generateRandomNumberBasedOnTime() {
        // 获取当前时间
        LocalDateTime now = LocalDateTime.now();

        // 提取月、日、时、分、秒
        int month = now.getMonthValue();
        int day = now.getDayOfMonth();
        int hour = now.getHour();
        int minute = now.getMinute();
        int second = now.getSecond();

        // 拼接成一个字符串
        String timeString = String.format("%02d%02d%02d%02d%02d", month, day, hour, minute, second);

        // 使用字符串的哈希值作为随机数的基础
        long hash = timeString.hashCode();
        // 使用 Random 生成一个四位的随机数
        Random random = new Random(hash);
        // 生成 0 到 9999 之间的随机数
        int randomPart = random.nextInt(10000);

        // 将时间部分和四位随机数拼接成一个字符串
        String randomNumberString = timeString + String.format("%04d", randomPart);

        // 将最终的字符串转换为 long 类型
        return Long.parseLong(randomNumberString);
    }
