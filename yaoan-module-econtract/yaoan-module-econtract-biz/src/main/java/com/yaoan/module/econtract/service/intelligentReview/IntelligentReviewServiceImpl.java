    /**
     * 将固定的 URL 转换为固定 10 位数字字符串
     *
     * @param url 输入 URL
     * @return 转换后的 10 位数字字符串
     */
    public static String convertUrlToString(String url) {
        try {
            // 选择哈希算法（如 MD5 或 SHA-256）
            MessageDigest md = MessageDigest.getInstance("MD5");
            // 计算 URL 的哈希值
            byte[] hashBytes = md.digest(url.getBytes());
            // 将哈希值转为 16 进制字符串
            String hexHash = new BigInteger(1, hashBytes).toString(16);

            // 仅保留数字部分
            StringBuilder numericHash = new StringBuilder();
            for (char ch : hexHash.toCharArray()) {
                if (Character.isDigit(ch)) {
                    numericHash.append(ch);
                } else {
                    // 将字母转换为数字（A=1, B=2, ..., F=6）
                    numericHash.append((ch - 'a' + 1));
                }
            }

            // 截取 10 位数字
            if (numericHash.length() > 10) {
                return numericHash.substring(0, 10);
            } else {
                // 不足 10 位，补零
                while (numericHash.length() < 10) {
                    numericHash.append("0");
                }
                return numericHash.toString();
            }

        } catch (Exception e) {
            throw new RuntimeException("Error converting URL to 10 digits", e);
        }
    }
